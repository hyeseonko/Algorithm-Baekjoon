#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>

int main()
{
	int T; scanf("%d", &T);
	while (T--)
	{
		int N, M; scanf("%d %d", &N, &M);
		while (M--)
		{
			int from, to; scanf("%d %d", &from, &to);
		}
		printf("%d\n", N - 1);
	}
	return 0;
}

